function capitalizeWords(str) {
  const newStr = ''
  for (let i = 0; i < str.length; i++){
    if (str[i] === 0 && str[i-1] === ' '){
      newStr += str[i].toUpperCase()
  } else {
      newStr += str[i].toLowerCase()
  }
}
  return newStr
}


